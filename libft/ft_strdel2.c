#include "libft.h"

void	ft_strdel2(char **as)
{
	int		i;

	i = -1;
	if (as)
	{
		if (as[0])
		{
			while (as[++i])
			{
				free(as[i]);
				as[i] = NULL;
			}
		}
		free(as);
		as = NULL;
	}
}
